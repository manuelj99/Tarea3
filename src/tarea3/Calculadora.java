    //funcion para evitar que se escriban letras
    public void restringir (KeyEvent evt){
        
        char c = evt.getKeyChar();
        
        if(c<'0' || c>'9' || c=='.'){
            evt.consume();
        }
        
    }
    
    //funcion para evitar el .00 cuando no es necesario
    public String nocero(Float resultado){
        String retorno="";
        retorno = String.valueOf(resultado);
        if(resultado%1==0){
            retorno = retorno.substring(0, retorno.length()-2);
        }
        return retorno;
    }
    
    
    //funcion para el historial
    public void historial(Float resultado){
        if(lblHist1.getText().isEmpty() || lblHist1.getText().equals("Historial de tus ultimas 3 operaciones")){
            if(txtEntrada.getText().equals("No se puede dividir entre 0!")){
                lblHist1.setText("Error!");
                return;
            }
            lblHist1.setText(nocero(numero1) + " " + operador + " " + nocero(numero2) + " = " + nocero(resultado));
        }
        else{
            if(lblHist2.getText().isEmpty()){
                if(txtEntrada.getText().equals("No se puede dividir entre 0!")){
                lblHist2.setText(lblHist1.getText());
                lblHist1.setText("Error!");
                return;
            }
            lblHist2.setText(lblHist1.getText());
            lblHist1.setText(nocero(numero1) + " " + operador + " " + nocero(numero2) + " = " + nocero(resultado));
            }
            else{
                if(txtEntrada.getText().equals("No se puede dividir entre 0!")){
                    lblHist3.setText(lblHist2.getText());
                    lblHist2.setText(lblHist1.getText());
                    lblHist1.setText("Error!");
                    return;
                }
                lblHist3.setText(lblHist2.getText());
                lblHist2.setText(lblHist1.getText());
                lblHist1.setText(nocero(numero1) + " " + operador + " " + nocero(numero2) + " = " + nocero(resultado));
            }
        }
    }
    
    //funcion para ejecutar el boton igual =
    public void igual(){
        numero2 = Float.parseFloat(txtEntrada.getText());
        switch(operador){
            case "+":        
                resultado = Float.parseFloat(nocero(numero1 + numero2));
                break;
            case "-":
                resultado = Float.parseFloat(nocero(numero1 - numero2));
                break;
            case "x":
                resultado = Float.parseFloat(nocero(numero1 * numero2));
                break;
            case "รท":
                if(numero2==0){
                    txtEntrada.setText("No se puede dividir entre 0!");
                }
                else{
                resultado = Float.parseFloat(nocero(numero1 / numero2));
                }
                break;          
        }
    }
    
    //funcion para los 4 operadores aritmeticos
    public void operador(String operadorAritmetico){
        if(!txtEntrada.getText().isEmpty()){
            numero1 = Float.parseFloat(txtEntrada.getText());
            operador = operadorAritmetico;
            txtEntrada.setText("");
        }
    }
    
    //funcion para evitar el cero a la izquierda
    public void numero(int numero){
        if(txtEntrada.getText().contains(".") || !txtEntrada.getText().equals("0")){
            txtEntrada.setText(txtEntrada.getText() + String.valueOf(numero));
            return;
        }
        if(txtEntrada.getText().equals("0")){
            txtEntrada.setText(String.valueOf(numero));
            return;
        }
    }
    
